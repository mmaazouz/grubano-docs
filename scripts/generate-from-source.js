#!/usr/bin/env node
/**
 * generate-from-source.js — Grubano docs auto-generator v2 (English source)
 *
 * Successor to generate-docs.js. Outputs ENGLISH source files into content/en/,
 * which scripts/translate-content.js then mirrors into fr/es/ar/it via Claude API.
 *
 * Reads the main app repo (located via APP_REPO_PATH env or sibling checkout)
 * and writes two parts of this docs site:
 *   1. content/en/api/*.mdx        ← parsed from app/api/ ** /route.ts
 *   2. content/en/changelog/index.mdx ← parsed from `git log`
 *
 * SCOPE: minimum viable. Per-route extraction is per-group (one MDX per
 * top-level resource, e.g. /api/orders/* → orders.mdx). Each endpoint shows:
 *   - HTTP methods
 *   - Path
 *   - Auth mode (public / session / CRON_SECRET)
 *   - JSDoc comment above the first export, or a placeholder
 *   - Detected query params (best-effort)
 *   - Detected response status codes
 *
 * DEFERRED (see Agent 5 brief): zod body-schema parsing, components/design-system
 * extraction, screenshots, interactive playground.
 *
 * Usage:
 *   APP_REPO_PATH=../grubano-kitchen-hub node scripts/generate-from-source.js
 */

const fs = require('fs')
const path = require('path')
const crypto = require('crypto')
const { execSync } = require('child_process')

// ── Locate repos ────────────────────────────────────────────────────────────

const DOCS_ROOT = path.resolve(__dirname, '..')
const CONTENT_DIR = path.join(DOCS_ROOT, 'content')
const LANG = 'en'
const OUT_DIR = path.join(CONTENT_DIR, LANG)

function resolveAppRepo() {
  const candidates = [
    process.env.APP_REPO_PATH,
    path.resolve(DOCS_ROOT, '..', 'grubano-kitchen-hub'),
    path.resolve(DOCS_ROOT, '..', 'grubano'),
  ].filter(Boolean)

  for (const c of candidates) {
    if (fs.existsSync(path.join(c, 'app', 'api'))) return path.resolve(c)
  }
  throw new Error(
    `Could not find the main app repo. Tried:\n  ${candidates.join('\n  ')}\n` +
      `Set APP_REPO_PATH to the grubano-kitchen-hub checkout.`,
  )
}

const APP_REPO = resolveAppRepo()
const API_DIR = path.join(APP_REPO, 'app', 'api')

console.log(`[generate-from-source] app repo : ${APP_REPO}`)
console.log(`[generate-from-source] output   : ${OUT_DIR}`)

// ── Helpers ──────────────────────────────────────────────────────────────────

const AUTOGEN_BANNER =
  '{/* AUTO-GENERATED by scripts/generate-from-source.js — do not edit by hand. */}'

function walk(dir, filter) {
  const out = []
  if (!fs.existsSync(dir)) return out
  for (const entry of fs.readdirSync(dir, { withFileTypes: true })) {
    const full = path.join(dir, entry.name)
    if (entry.isDirectory()) out.push(...walk(full, filter))
    else if (filter(full)) out.push(full)
  }
  return out
}

/** Turn an absolute route.ts path into a public endpoint, e.g. /api/orders/{id}/status */
function routeToEndpoint(filePath) {
  const rel = path
    .relative(path.join(APP_REPO, 'app'), filePath)
    .replace(/\\/g, '/')
    .replace(/\/route\.ts$/, '')
  return (
    '/' +
    rel
      .split('/')
      .map((seg) =>
        seg.replace(/^\[\.\.\.(.+)\]$/, '{...$1}').replace(/^\[(.+)\]$/, '{$1}'),
      )
      .join('/')
  )
}

/** Top-level resource group, e.g. /api/loyalty/register -> "loyalty" */
function endpointGroup(endpoint) {
  const parts = endpoint.split('/').filter(Boolean) // ["api", "loyalty", ...]
  return parts[1] || 'misc'
}

const HTTP_METHODS = ['GET', 'POST', 'PUT', 'PATCH', 'DELETE']

function extractMethods(src) {
  return HTTP_METHODS.filter((m) =>
    new RegExp(`export\\s+(async\\s+)?function\\s+${m}\\b`).test(src),
  )
}

function extractAuth(src) {
  if (/CRON_SECRET/.test(src)) return 'CRON_SECRET'
  if (/getToken|getServerSession|authOptions/.test(src)) return 'session'
  return 'public'
}

/** Query params referenced via searchParams.get('x') */
function extractQueryParams(src) {
  const params = new Set()
  for (const m of src.matchAll(/searchParams\.get\(\s*['"`]([^'"`]+)['"`]\s*\)/g)) {
    params.add(m[1])
  }
  return [...params]
}

function extractStatusCodes(src) {
  const codes = new Set()
  for (const m of src.matchAll(/status:\s*(\d{3})/g)) codes.add(Number(m[1]))
  if (/NextResponse\.json\(/.test(src)) codes.add(200)
  return [...codes].sort((a, b) => a - b)
}

/**
 * Extract a brief description for the route. Tries (in order):
 *   1. The first JSDoc /** ... *​/ block in the file
 *   2. The first `//` line comment at the top of the file
 *   3. A generated placeholder
 */
function extractDescription(src, endpoint) {
  // JSDoc
  const jsdoc = src.match(/^\s*\/\*\*\s*([\s\S]*?)\s*\*\//m)
  if (jsdoc) {
    const cleaned = jsdoc[1]
      .split('\n')
      .map((l) => l.replace(/^\s*\*\s?/, '').trim())
      .filter((l) => l && !l.startsWith('@'))
      .join(' ')
      .trim()
    if (cleaned) return cleaned
  }
  // Leading // comments
  const lines = src.split('\n')
  const leading = []
  for (const l of lines) {
    const t = l.trim()
    if (t.startsWith('//')) leading.push(t.replace(/^\/\/\s?/, ''))
    else if (t === '' && leading.length === 0) continue
    else break
  }
  if (leading.length) return leading.join(' ').trim()
  // Placeholder
  return `Auto-detected endpoint at \`${endpoint}\`. Description not yet documented.`
}

// ── English label tables ─────────────────────────────────────────────────────

const AUTH_BADGE = {
  public: 'Public',
  session: 'Session required',
  CRON_SECRET: 'CRON_SECRET',
}

const STATUS_TEXT = {
  200: 'OK',
  201: 'Created',
  204: 'No content',
  400: 'Bad request',
  401: 'Authentication required',
  403: 'Forbidden',
  404: 'Not found',
  409: 'Conflict',
  422: 'Unprocessable entity',
  500: 'Server error',
}

// ── Parse all routes ──────────────────────────────────────────────────────────

const routeFiles = walk(API_DIR, (f) => f.endsWith('route.ts'))

const routes = routeFiles.map((file) => {
  const src = fs.readFileSync(file, 'utf8')
  const endpoint = routeToEndpoint(file)
  return {
    file,
    endpoint,
    group: endpointGroup(endpoint),
    methods: extractMethods(src),
    auth: extractAuth(src),
    query: extractQueryParams(src),
    statuses: extractStatusCodes(src),
    description: extractDescription(src, endpoint),
  }
})

// group -> routes[]
const groups = {}
for (const r of routes) {
  ;(groups[r.group] ||= []).push(r)
}
for (const g of Object.keys(groups)) {
  groups[g].sort((a, b) => a.endpoint.localeCompare(b.endpoint))
}

// ── Renderers (English) ──────────────────────────────────────────────────────

function frontmatter(fields) {
  const lines = ['---']
  for (const [k, v] of Object.entries(fields)) {
    // Quote values containing special chars
    const needsQuote = /[:"#&*!|>%@`{}[\],?\-]/.test(String(v)) || String(v).includes(' ')
    lines.push(`${k}: ${needsQuote ? JSON.stringify(v) : v}`)
  }
  lines.push('---')
  return lines.join('\n')
}

function renderGroupPage(group, groupRoutes, sourceHash) {
  const title = group.charAt(0).toUpperCase() + group.slice(1)
  const lines = []
  lines.push(
    frontmatter({
      title: `API — ${title}`,
      description: `Reference for /api/${group} endpoints`,
      sourceHash,
    }),
  )
  lines.push('')
  lines.push(AUTOGEN_BANNER)
  lines.push('')
  lines.push(`# API — ${title}`)
  lines.push('')
  lines.push(`${groupRoutes.length} endpoint(s) under \`/api/${group}\`.`)
  lines.push('')

  for (const r of groupRoutes) {
    lines.push(`## \`${r.methods.join(' · ') || 'GET'}\` \`${r.endpoint}\``)
    lines.push('')
    lines.push(r.description)
    lines.push('')
    lines.push(`**Auth:** ${AUTH_BADGE[r.auth]}`)
    lines.push('')

    if (r.query.length) {
      lines.push('**Query parameters**')
      lines.push('')
      lines.push('| Parameter | Source |')
      lines.push('|---|---|')
      for (const q of r.query) lines.push(`| \`${q}\` | query string |`)
      lines.push('')
    }

    if (r.statuses.length) {
      lines.push('**Responses**')
      lines.push('')
      lines.push('| Code | Meaning |')
      lines.push('|---|---|')
      for (const s of r.statuses) lines.push(`| \`${s}\` | ${STATUS_TEXT[s] || ''} |`)
      lines.push('')
    }
  }

  return lines.join('\n') + '\n'
}

function renderApiIndex(sourceHash) {
  const lines = []
  lines.push(
    frontmatter({
      title: 'API Reference',
      description: 'Overview of the Grubano REST API',
      sourceHash,
    }),
  )
  lines.push('')
  lines.push(AUTOGEN_BANNER)
  lines.push('')
  lines.push('# API Reference')
  lines.push('')
  lines.push(
    `The Grubano REST API exposes **${routes.length} endpoints**. Request bodies are validated with [Zod](https://zod.dev) and responses are JSON.`,
  )
  lines.push('')
  lines.push('## All endpoints')
  lines.push('')
  lines.push('| Methods | Endpoint | Auth |')
  lines.push('|---|---|---|')
  for (const r of [...routes].sort((a, b) => a.endpoint.localeCompare(b.endpoint))) {
    lines.push(
      `| \`${r.methods.join(' · ') || 'GET'}\` | \`${r.endpoint}\` | ${AUTH_BADGE[r.auth]} |`,
    )
  }
  lines.push('')
  return lines.join('\n') + '\n'
}

function renderApiMeta(groupNames) {
  const lines = []
  lines.push(
    '// AUTO-GENERATED by scripts/generate-from-source.js — do not edit by hand.',
  )
  lines.push("import type { MetaRecord } from 'nextra'")
  lines.push('')
  lines.push('export default {')
  lines.push("  index: 'Overview',")
  for (const g of groupNames) {
    const title = g.charAt(0).toUpperCase() + g.slice(1)
    lines.push(`  ${JSON.stringify(g)}: ${JSON.stringify(title)},`)
  }
  lines.push('} satisfies MetaRecord')
  return lines.join('\n') + '\n'
}

// ── Changelog (English) ──────────────────────────────────────────────────────

const TYPE_LABEL = {
  feat: '✨ Features',
  fix: '🐛 Fixes',
  docs: '📝 Documentation',
  chore: '🔧 Maintenance',
  refactor: '♻️ Refactoring',
  perf: '⚡ Performance',
  test: '✅ Tests',
  style: '💅 Style',
}

function renderChangelog(sourceHash) {
  let raw = ''
  try {
    raw = execSync('git log -50 --date=short --pretty=format:%ad%x09%s', {
      cwd: APP_REPO,
      encoding: 'utf8',
    })
  } catch (err) {
    console.warn('[generate-from-source] git log failed:', err.message)
    raw = ''
  }

  const byDate = new Map() // date -> [{type, scope, subject}]
  for (const line of raw.split('\n').filter(Boolean)) {
    const [date, subject = ''] = line.split('\t')
    const m = subject.match(/^(\w+)(?:\(([^)]+)\))?:\s*(.+)$/)
    const entry = m
      ? { type: m[1], scope: m[2] || null, subject: m[3] }
      : { type: 'other', scope: null, subject }
    if (!byDate.has(date)) byDate.set(date, [])
    byDate.get(date).push(entry)
  }

  const lines = []
  lines.push(
    frontmatter({
      title: 'Changelog',
      description: 'Grubano platform release history',
      sourceHash,
    }),
  )
  lines.push('')
  lines.push(AUTOGEN_BANNER)
  lines.push('')
  lines.push('# Changelog')
  lines.push('')
  lines.push('Auto-generated from commits on the main application repository.')
  lines.push('')

  const dates = [...byDate.keys()].sort().reverse()
  for (const date of dates) {
    lines.push(`## ${date}`)
    lines.push('')
    const entries = byDate.get(date)
    const byType = {}
    for (const e of entries) (byType[e.type] ||= []).push(e)
    const typeOrder = Object.keys(TYPE_LABEL)
      .filter((t) => byType[t])
      .concat(byType.other ? ['other'] : [])
    for (const type of typeOrder) {
      lines.push(`**${TYPE_LABEL[type] || '📦 Other'}**`)
      lines.push('')
      for (const e of byType[type]) {
        const scope = e.scope ? `**${e.scope}** — ` : ''
        lines.push(`- ${scope}${e.subject}`)
      }
      lines.push('')
    }
  }

  if (!dates.length) lines.push('_No commits found._\n')

  return lines.join('\n') + '\n'
}

function renderChangelogMeta() {
  return (
    '// AUTO-GENERATED by scripts/generate-from-source.js — do not edit by hand.\n' +
    "import type { MetaRecord } from 'nextra'\n\n" +
    'export default {\n' +
    "  index: 'Changelog',\n" +
    '} satisfies MetaRecord\n'
  )
}

// ── Write everything ──────────────────────────────────────────────────────────

function writeFile(relPath, contents) {
  const full = path.join(OUT_DIR, relPath)
  fs.mkdirSync(path.dirname(full), { recursive: true })
  fs.writeFileSync(full, contents, 'utf8')
  console.log(`[generate-from-source] wrote ${path.relative(DOCS_ROOT, full)}`)
}

function sha(s) {
  return crypto.createHash('sha256').update(s).digest('hex').slice(0, 16)
}

const groupNames = Object.keys(groups).sort()

// Hash the source material so the translator can detect changes idempotently.
// We hash the parsed-route summary (not raw source) so cosmetic source changes
// that don't affect docs don't bust the cache.
const apiIndexHash = sha(JSON.stringify(routes.map((r) => ({ ...r, file: undefined }))))
writeFile('api/index.mdx', renderApiIndex(apiIndexHash))
writeFile('api/_meta.ts', renderApiMeta(groupNames))
for (const g of groupNames) {
  const groupHash = sha(JSON.stringify(groups[g].map((r) => ({ ...r, file: undefined }))))
  writeFile(`api/${g}.mdx`, renderGroupPage(g, groups[g], groupHash))
}

let gitLogRaw = ''
try {
  gitLogRaw = execSync('git log -50 --date=short --pretty=format:%ad%x09%s', {
    cwd: APP_REPO,
    encoding: 'utf8',
  })
} catch {
  // ignore; renderChangelog handles the empty case
}
writeFile('changelog/index.mdx', renderChangelog(sha(gitLogRaw)))
writeFile('changelog/_meta.ts', renderChangelogMeta())

console.log(
  `[generate-from-source] done — ${routes.length} endpoints across ${groupNames.length} groups.`,
)
